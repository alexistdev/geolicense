package com.alexistdev.geolicense.models.repository;

import com.alexistdev.geolicense.models.entity.Payment;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;

import java.util.Optional;
import java.util.UUID;

public interface PaymentRepo extends JpaRepository<Payment, UUID> {

    @Query("SELECT p FROM Payment p WHERE p.orders.id = :orderId")
    Optional<Payment> findByOrdersId(@Param("orderId") UUID orderId);
}
